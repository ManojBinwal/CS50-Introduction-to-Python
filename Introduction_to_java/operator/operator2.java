class Operator2 {
    public static void main(String[] args) {
        int a = 12;
        float b = 3.1f;
        char c = 'a';
        double d = 14.13d;

        System.out.println(a/b);   //3.8709679   int / float  = float
        System.out.println(b*c);   //300.69998   char * float = float
        System.out.println(b+d);    //17.299999  float + double = double

    }
}